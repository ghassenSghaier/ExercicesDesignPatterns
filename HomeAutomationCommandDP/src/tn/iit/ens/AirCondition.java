package tn.iit.ens;

public class AirCondition {

	public void up() {
		System.out.println("temperature increased");
	}

	public void down() {
		System.out.println("temperature decreased");
	}

	public void stop() {
		System.out.println("air condition stopped");
	}

	public void lightOn() {
		System.out.println("air condition light on");
	}

	public void lightOff() {
		System.out.println("air condition light off");
	}

}
